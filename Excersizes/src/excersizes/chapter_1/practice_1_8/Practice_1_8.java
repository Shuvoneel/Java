package excersizes.chapter_1.practice_1_8;

public class Practice_1_8 {

    static double pi = 3.141597;
    static double radius = 5.5;
    static double perimeter = (2 * radius * pi);   // perimeter of circle
    static double area = (radius * radius * pi);   // area of circle

    public static void main(String[] args) {
        System.out.println("The perimeter is: " + perimeter);
        System.out.println("The area is: " + area);
    }
}
