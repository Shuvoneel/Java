package dec_22.page120;

public class CommonMathematicalFunctions {

    public static void main(String[] args) {
        System.out.println("Square Root of 25 is: " + Math.sqrt(25));

        System.out.println("Math Ceil: " + Math.ceil(25.123654));

        System.out.println("Math Floor: " + Math.floor(25.123654));

        System.out.println("Math Rint: " + Math.rint(25.123654));

        System.out.println("Math Rint: " + Math.rint(25.5));

        System.out.println("Math Random: " + Math.random());
    }
}
