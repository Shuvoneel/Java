package dec_22.page127;

public class Casting {

    public static void main(String[] args) {
        int a = (int) 'S';
        int b = (int) 'H';
        int c = (int) 'U';
        int d = (int) 'V';
        int e = (int) 'O';
        System.out.println("Shuvo in number: " + a + " " + b + " " + c + " " + d + " " + e);

        char s = (char) 83;
        char h = (char) 72;
        char u = (char) 85;
        char v = (char) 86;
        char o = (char) 79;
        System.out.println(s + "" + h + "" + u + "" + v + "" + o);
    }
}
